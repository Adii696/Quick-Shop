const express = require('express');
const router = express.Router();
const multer = require('multer');
const path = require('path');
const auth = require('../middleware/auth');
const admin = require('../middleware/admin');
const storage = multer.diskStorage({ destination: function (req, file, cb) { cb(null, 'uploads/'); }, filename: function (req, file, cb) { cb(null, Date.now() + path.extname(file.originalname)); } });
const upload = multer({ storage });
router.post('/', auth, admin, upload.single('image'), (req,res)=>{ if(!req.file) return res.status(400).json({msg:'No file'}); res.json({url:'/uploads/'+req.file.filename}); });
module.exports = router;
